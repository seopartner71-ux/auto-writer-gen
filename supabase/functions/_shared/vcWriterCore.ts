export function detectRepeatedOpeners(md: string): { ok: boolean; offenders: Array<{ opener: string; count: number }> } {
  const paras = md.split(/\n{2,}/).map((p) => p.trim()).filter((p) => p && !/^#{1,6}\s/.test(p) && !/^[->|]/.test(p) && !/^P\.?\s*S\.?/i.test(p));
  const openers: Record<string, number> = {};
  for (const p of paras) {
    const first = p.split(/\s+/)[0]?.toLowerCase().replace(/[^a-zа-я-]/gi, "");
    if (!first || first.length < 2) continue;
    openers[first] = (openers[first] || 0) + 1;
  }
  const offenders = Object.entries(openers)
    .filter(([_, n]) => n >= 3)
    .map(([opener, count]) => ({ opener, count }))
    .sort((a, b) => b.count - a.count);
  return { ok: offenders.length === 0, offenders };
}

export function stripDuplicateH1(md: string, title: string): string {
  return _stripDuplicateH1Impl(md, title);
}

function _stripDuplicateH1Impl(md: string, title: string): string {
  const norm = (s: string) => s.trim().toLowerCase().replace(/\s+/g, " ");
  const normTitle = norm(title);
  const out = md.split("\n").filter((line) => {
    if (!/^#\s+/.test(line)) return true;
    const h = norm(line.replace(/^#\s+/, ""));
    return h !== normTitle;
  }).join("\n");
  // Demote any remaining single # to ## (vc.ru already рендерит title отдельно)
  return out.replace(/^#\s+/gm, "## ");
}

/**
 * Coherence Guard: проверяет, что именованные сущности из лида (суммы денег,
 * упомянутые роли вроде "клиент агентства") реально продолжаются в теле статьи.
 * Если в лиде есть сумма потерь / "у клиента N произошло X", а дальше в тексте
 * ни эта сумма, ни сцена не появляются — лид «оторван» от тела (классический
 * фейл LLM при сборке хук+гайд).
 */
export function detectIncoherentLead(md: string): { ok: boolean; reason?: string; lead?: string } {
  const paras = md.split(/\n{2,}/).map((p) => p.trim()).filter(Boolean);
  let lead = "";
  for (const p of paras) {
    if (/^#{1,6}\s/.test(p) || /^!\[/.test(p) || /^>\s/.test(p)) continue;
    lead = p;
    break;
  }
  if (!lead || lead.length < 60) return { ok: true };
  const body = md.slice(md.indexOf(lead) + lead.length);
  if (body.length < 500) return { ok: true };
  // Извлекаем «маркеры» лида: сумма денег и якорная сцена.
  const moneyMatch = lead.match(/(\d[\d\s.,]{1,9})\s?(?:руб|₽|тыс|млн|долл|евро|\$|€)/i);
  if (moneyMatch) {
    const num = moneyMatch[1].replace(/\s+/g, "");
    const bodyHasNum = new RegExp(num.replace(/[.,]/g, "[.,]")).test(body.replace(/\s+/g, ""));
    if (!bodyHasNum) {
      return { ok: false, reason: `сумма из лида (${moneyMatch[0].trim()}) не упоминается в теле`, lead };
    }
  }
  // Сценная привязка лида (агентство/клиент/корпоратив/event) без отражения в теле.
  const sceneTriggers = /(агентств\w+|корпоратив\w*|event[-\s]?индустри|поставщик\w*|подрядчик\w*|VIP-гост\w*)/i;
  const leadScene = lead.match(sceneTriggers);
  if (leadScene) {
    const word = leadScene[0].toLowerCase().slice(0, 6);
    const bodyHasScene = new RegExp(word.replace(/[.*+?^${}()|[\]\\]/g, "\\$&"), "i").test(body);
    if (!bodyHasScene) {
      return { ok: false, reason: `сцена лида ("${leadScene[0]}") не развита в статье`, lead };
    }
  }
  return { ok: true };
}

const CLICHE_RE = /\s*(в\s+современных\s+реалиях|на\s+сегодняшний\s+день|не\s+секрет,?\s+что|давайте\s+(разберемся|разберем|погрузимся|поговорим)|итак,?\s+(подведем\s+итоги|давайте)|стоит\s+отметить,?\s+что|следует\s+(подчеркнуть|отметить),?\s+что|нельзя\s+не\s+упомянуть|в\s+заключение\s+хочется\s+(сказать|отметить|подчеркнуть)|подводя\s+(итог|черту)|как\s+мы\s+уже\s+говорили|важно\s+(понимать|помнить|отметить),?\s+что|в\s+(современном|сегодняшнем)\s+(быстро\s+меняющемся\s+)?мире|безусловно,?\s+|крайне\s+важно,?\s+|хочется\s+(отметить|подчеркнуть|сказать),?\s+что|погрузиться\s+в\s+(тему|мир|вопрос)|динамично\s+развивающа(я|е)ся\s+компания|индивидуальный\s+подход\s+к\s+каждому(\s+клиенту)?|комплексн(ое|ый)\s+(решение|подход))\s*[,.]?\s*/gi;

// Эмодзи: ограничиваем количество эмодзи во всём тексте (vc.ru-аудитория не любит спам смайлами).
// Используем Extended_Pictographic — покрывает все смайлы, символы, флаги, ZWJ-склейки.
const EMOJI_RE = /\p{Extended_Pictographic}/gu;
export function limitEmojis(md: string, max = 2): { md: string; removed: number } {
  let kept = 0;
  let removed = 0;
  const out = (md || "").replace(EMOJI_RE, (m) => {
    if (kept < max) { kept++; return m; }
    removed++;
    return "";
  });
  return { md: out.replace(/[ \t]{2,}/g, " "), removed };
}

/**
 * Считает абзацы с количеством предложений > maxSentences.
 * Игнорирует заголовки, списки, цитаты, картинки и P.S.
 * Возвращает количество нарушителей и их сэмплы (для подсказки в checklist).
 */
export function detectLongParagraphs(
  md: string,
  maxSentences = 5,
): { ok: boolean; offenders: number; samples: Array<{ sentences: number; preview: string }> } {
  const blocks = (md || "").split(/\n{2,}/);
  const samples: Array<{ sentences: number; preview: string }> = [];
  let offenders = 0;
  for (const raw of blocks) {
    const b = raw.trim();
    if (!b) continue;
    // пропускаем нетекстовые блоки
    if (/^#{1,6}\s/.test(b)) continue;
    if (/^[-*+]\s/m.test(b) && !/\n[^\-\*\+\s]/.test(b)) continue;
    if (/^>\s/.test(b)) continue;
    if (/^!\[/.test(b)) continue;
    if (/^P\.?S\.?[\s:]/i.test(b)) continue;
    // считаем предложения: .!? с пробелом/концом, плюс минимальная длина блока
    if (b.length < 80) continue;
    const matches = b.match(/[.!?…]+(?=\s|$)/g) || [];
    const sentences = matches.length;
    if (sentences > maxSentences) {
      offenders++;
      if (samples.length < 3) {
        samples.push({ sentences, preview: b.slice(0, 120).replace(/\s+/g, " ") + (b.length > 120 ? "…" : "") });
      }
    }
  }
  return { ok: offenders === 0, offenders, samples };
}

export function stripCliches(md: string): { md: string; removed: number } {
  let count = 0;
  const out = md.replace(CLICHE_RE, (m) => { count++; return " "; })
    .replace(/[ \t]{2,}/g, " ")
    .replace(/\n{3,}/g, "\n\n")
    .replace(/\s+([.,;:!?])/g, "$1");
  return { md: out, removed: count };
}

/**
 * Анти-симметрия: если в списке >=3 подряд идущих пункта имеют шаблон
 * "- Заголовок: одно короткое предложение", меняем двоеточие на " - "
 * в каждом таком пункте чередуя стиль (через один), чтобы убрать визуальную
 * однородность. Это убирает паттерн, который ловит symmetry-detector.
 */
export function breakListSymmetry(md: string): string {
  const lines = md.split("\n");
  const PATTERN = /^(\s*[-*]\s+)([A-ЯЁA-Z][^:\n]{2,40}):\s+(.+)$/;
  let runStart = -1;
  const apply = (from: number, to: number) => {
    // Чередуем: каждый второй пункт превращаем ":" -> " -".
    let toggle = 0;
    for (let k = from; k < to; k++) {
      const m = lines[k].match(PATTERN);
      if (!m) continue;
      if (toggle % 2 === 1) {
        lines[k] = `${m[1]}${m[2]} - ${m[3]}`;
      }
      toggle++;
    }
  };
  for (let i = 0; i <= lines.length; i++) {
    const isItem = i < lines.length && PATTERN.test(lines[i]);
    if (isItem) {
      if (runStart === -1) runStart = i;
    } else {
      if (runStart !== -1 && i - runStart >= 3) apply(runStart, i);
      runStart = -1;
    }
  }
  return lines.join("\n");
}

export interface SeoReport {
  ok: boolean;
  issues: string[];
  inTitle: boolean;
  inFirstH2: boolean;
  occurrences: number;
}

export function validateSeo(md: string, title: string, targetQuery: string): SeoReport {
  if (!targetQuery) return { ok: true, issues: [], inTitle: true, inFirstH2: true, occurrences: 0 };
  const q = targetQuery.toLowerCase().trim();
  const tokens = q.split(/\s+/).filter((t) => t.length > 3);
  const textLow = md.toLowerCase();
  const titleLow = title.toLowerCase();
  const inTitle = titleLow.includes(q) || (tokens.length > 0 && tokens.every((t) => titleLow.includes(t)));
  const h2s = (md.match(/^##\s+.+$/gm) || []).map((s) => s.toLowerCase());
  const inFirstH2 = !!h2s[0] && (h2s[0].includes(q) || tokens.some((t) => h2s[0].includes(t)));
  const head = tokens[0] || q;
  const occurrences = (textLow.match(new RegExp(head.replace(/[.*+?^${}()|[\]\\]/g, "\\$&"), "g")) || []).length;
  const issues: string[] = [];
  if (!inTitle) issues.push("title_no_keyword");
  if (!inFirstH2) issues.push("first_h2_no_keyword");
  if (occurrences < 4) issues.push(`low_density:${occurrences}`);
  return { ok: issues.length === 0, issues, inTitle, inFirstH2, occurrences };
}

